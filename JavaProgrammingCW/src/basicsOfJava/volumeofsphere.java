package basicsOfJava;

public class volumeofsphere {
    public static void main(String[] args) {
        double pi=3.1415;
        double radius=1;
        double x=4;
        double y=3;
        double z=x/y;
        double volume= z*pi*radius*radius*radius; // Formula for volume of sphere --> v=4/3*pi*r*r*r
        System.out.println("The Volume Of Sphere is :"+volume);
    }
}
