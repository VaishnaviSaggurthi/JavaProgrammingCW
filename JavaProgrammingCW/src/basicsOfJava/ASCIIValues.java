package basicsOfJava;

public class ASCIIValues {
    public static void main(String[] args) {
        char ch='a';
        int x = (int)ch; // TYPE CASTING --> From one data type into another data type
        System.out.println("The ASCII Value of a is :"+x);
        char b ='A';
        int y=(int)b;
        System.out.println("The ASCII Value of A is :"+y);
    }
}
