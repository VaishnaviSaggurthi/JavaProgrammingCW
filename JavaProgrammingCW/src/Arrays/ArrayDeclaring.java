package Arrays;

public class ArrayDeclaring {
    public static void main(String[] args) {
        int[] ages = new int[5];
        ages[0] = 5;
        ages[1] = 10;
        ages[2] = 15;
        ages[3] = 20;
        ages[4] = 25;
        System.out.println( ages[0]);
        System.out.println( ages[1]);
        System.out.println( ages[2]);
        System.out.println( ages[3]);
        System.out.println( ages[4]);
    }
}
