package JavaDSA.LinkedList;

public class EvidentLimitations {
    // Limitations Of Linked List :
    // 1) Elements can be accessed in sequential manner not in a random order
    // 2) Difficult to traverse i.e. time complexity
    // 3) Linear Search is only possible but not Binary Search in case of Linked Lists
    // 4) Extra pointer is assigned for each element to access
    // 5) insertAtEnd --> T.C = O(n) if only head is given
    // 6) insertAtGivenIndex --> T.C = O(n)
}
