package OOPsofJava;
public class BuiltInMethods {
    public static void main(String[] args) {
        // Math file is need not be imported
        System.out.println(Math.sqrt(24)); // sqrt
        System.out.println(Math.sqrt(16)); // sqrt

        System.out.println(Math.floor(5.3)); // floor --> 5.0
        System.out.println(Math.floor(5.8)); // floor --> 5.0
        System.out.println(Math.ceil(5.3)); // ceil --> 6.0
        System.out.println(Math.ceil(5.8)); // ceil --> 6.0

        System.out.println(Math.max(3,5)); // maximum --> 5
        System.out.println(Math.min(2,1)); // minimum --> 1
        System.out.println();
    }
}
